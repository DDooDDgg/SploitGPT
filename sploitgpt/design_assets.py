def get_banner(banner_type: str = "main") -> str:
    """Get ASCII banner for specified type.
    
    Args:
        banner_type: Banner type (main, recon, enumeration, etc.)
        
    Returns:
        ASCII art banner as string
    """
    banner_enum = BannerType(banner_type.lower()) if banner_type.lower() in [b.value for b in BannerType] else BannerType.MAIN
    return BANNERS[banner_enum]["art"]


def get_banner_styled(banner_type: str = "main") -> str:
    """Get styled ASCII banner with colors.
    
    Args:
        banner_type: Banner type (main, recon, enumeration, etc.)
        
    Returns:
        Styled banner with Rich markup
    """
    banner_enum = BannerType(banner_type.lower()) if banner_type.lower() in [b.value for b in BannerType] else BannerType.MAIN
    banner_data = BANNERS[banner_enum]
    
    lines = banner_data["art"].strip().split('\n')
    styled_lines = []
    
    for line in lines:
        if '[' in line and ']' in line:  # Subtitle line
            styled_lines.append(f"[{banner_data['subtitle_color']}]{line}[/]")
        else:
            styled_lines.append(f"[{banner_data['color']}]{line}[/]")
    
    return '\n'.join(styled_lines)


